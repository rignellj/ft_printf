#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*d;
	char	*s;
	size_t	i;

	if (src == dst)
		return (dst);
	d = (char*)dst;
	s = (char*)src;
	if (src < dst)
	{
		i = len;
		while (i-- > 0)
			d[i] = s[i];
	}
	else
	{
		i = 0;
		while (i < len)
		{
			d[i] = s[i];
			++i;
		}
	}
	return (dst);
}
